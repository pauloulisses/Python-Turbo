# Função Range

print(range(1, 10))

# Imprimindo de 1 até 10
for number in range(1, 11):
    print(number)

# Irá imprimir de 2 em 2
for numero in range(1, 11, 2):
    print(numero)


# Exercicio
# contando de 1 até 100

for numbers in range(1, 101):
    print(numbers)

# somando de 1 até 100

total = 0
for num in range(1, 101):
    total += num
print(total)
