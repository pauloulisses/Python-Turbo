# Lista de frutas
frutas = ['Uva', 'Manga', 'Laranja', 'Goiaba']

# Loop for para percorrer cada fruta na lista
for fruta in frutas:
    print(fruta)  # Imprime o nome da fruta
    print(fruta + ' Jesus te ama')  # Imprime uma mensagem junto com o nome da fruta


